function darkfundo(){
    let darkfundo = document.querySelector("body");
    darkfundo.style.backgroundColor = "black";
    darkfundo.style.transition = "1.5s";
}
function whitefundo(){
   let whitefundo = document.querySelector("body");
   whitefundo.style.transition = "1.5s";
   whitefundo.style.backgroundColor = "white";
}
